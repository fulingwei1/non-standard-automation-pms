-- 统计各类别产品数量
SELECT
    c.name AS '类别名称',
    COUNT(p.id) AS '产品数量'
FROM advantage_product_categories c
LEFT JOIN advantage_products p ON c.id = p.category_id
WHERE c.is_active = 1
GROUP BY c.id, c.name
ORDER BY c.sort_order;

-- 显示总计
SELECT
    '总计' AS '类别名称',
    COUNT(*) AS '产品数量'
FROM advantage_products
WHERE is_active = 1;

